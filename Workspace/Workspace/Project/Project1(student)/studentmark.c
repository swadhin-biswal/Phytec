#include <stdio.h>

int studentmarks1(int a )
{
    if(a>=90 && a<=100)
    {
        printf("You have scored grade O in physics.....");
    }
    else if(a>=80 && a<=90)
    {
        printf("You have scored grade E in physics.....");
    }
    else if(a>=70 && a<=80)
    {
        printf("You have scored grade A in physics.....");
    }
    else if(a>=60 && a<=70)
    {
        printf("You have scored grade B in physics.....");
    }
    else if(a>=50 && a<=60)
    {
        printf("You have scored grade C in physics.....");
    }
    
    else if(a>=40 && a<=50)
    {
        printf("You have scored grade D in physics.....");
    }
    else
    {
        printf("Needs improvement...FAIL!!!");
    }
}
